void Timer1Init(void)	//10毫秒@20.000MHz
{
	AUXR &= 0xBF;		//定时器时钟12T模式
	TMOD &= 0x0F;		//设置定时器模式
	TL1 = 0xE5;		//设置定时初值
	TH1 = 0xBE;		//设置定时初值
	TF1 = 0;			//清除TF1标志
	TR1 = 1;			//定时器1开始计时
	ET1 = 1;        	//允许定时器T1中断	  
}





